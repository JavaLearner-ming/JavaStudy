package Sample;


public class demo4 {
    /*
        关系运算符【比较运算符】：( > )   ( >= )  ( < )   ( <= )  ( == )  ( != )
        返回结果boolean类型： ture / false
        注意 " == " 和 " = " 的区别
     */

    public static void main(String[] args) {
        System.out.println(20 > 10);    // ture
        System.out.println(100 < 20);   // false
        System.out.println(100 <= 20);  // false
        System.out.println(100 >= 20);  // ture
        System.out.println(100 == 20);  // false
        System.out.println(100 != 20);  // ture


    }
}
