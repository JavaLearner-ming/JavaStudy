package Sample;

/*
    使用for 循环 打印 1-5和 5-1
 */
public class demo13 {
    public static void main(String[] args) {
        for(int i=1; i<=5; i++){
            System.out.println(i);
        }
        System.out.println("--------------");
        for(int n=5; n>=1; n--){
            System.out.println(n);
        }
    }
}
